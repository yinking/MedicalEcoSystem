    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        int selectedRow = tlbNetwork.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please Select a Row of Network");
            return;
        }
        int enterpriseRow = tlbEnterprise.getSelectedRow();
        if (enterpriseRow < 0) {
            JOptionPane.showMessageDialog(null, "Please Select a Row of Enterprise");
            return;
        }
        Enterprise enterprise = (Enterprise) tlbEnterprise.getValueAt(enterpriseRow, 0);
        
        User user = new User(tfUsername.getText(), tfUsername.getText(), enterprise.getIdEnterprise(), (String) cbUserRole.getSelectedItem());
        UserDao dao = new UserDao();
        dao.save(user);
        JOptionPane.showMessageDialog(null, "congratulations, you have created a new user");

        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

