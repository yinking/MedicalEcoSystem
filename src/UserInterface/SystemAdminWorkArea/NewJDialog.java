    private void tlbNetworkMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tlbNetworkMouseClicked
        
        int index = tlbNetwork.getSelectedRow();
        City city = (City) tlbNetwork.getValueAt(index, 0);
        
        EnterpriseDao dao = new EnterpriseDao();
        DefaultTableModel dtm = new DefaultTableModel();
        dtm.addColumn("Name");
        dtm.addColumn("Type");
        
        for (Enterprise enterprise : dao.searchEnterpriseByIdCity(city.getId() + "")) {
            dtm.addRow(new Object[]{enterprise, enterprise.getType()});
        }
        tlbEnterprise.setModel(dtm);

        // TODO add your handling code here:
    }//GEN-LAST:event_tlbNetworkMouseClicked

